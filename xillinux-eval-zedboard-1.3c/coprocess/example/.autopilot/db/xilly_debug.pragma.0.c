void xilly_puts(const char *str) {
  while (*str) {
    while (!(debug_ready & 1)); // Wait for ready signal
    debug_out = *str++;
  }
}

void xilly_hexprint(const uint32_t val, const int digits) {
  char out[9];
  int i;
  int first = 8 - digits;
  uint32_t v = val;

  for (i=7; i>=0; i--, v=v>>4) {
    char x = v & 0xf;
    out[i] = (x < 10) ? 48 + x : 87 + x;
    if ((x != 0) && (first > i))
      first = i;
  }
  out[8] = 0;

  xilly_puts(out + first);
}

